export const getClassName = (n) => {
    if (n <= 0) return 'blank-cell'
    if (n === 1) return 'i-cell'
    if (n === 2) return 'o-cell'
    if (n === 3) return 'l-cell'
    if (n === 4) return 'j-cell'
    if (n === 5) return 'z-cell'
    if (n === 6) return 's-cell'
    if (n === 7) return 't-cell'


}